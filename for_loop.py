my_list=["jake","tom","xiao li"]

# 打印出list中所有的数据；'name'可以随便写
for name in my_list:
    print(name)
# 打印出0-10
for number in range(10):
    print(number)
# 打印出10到20
for number in range(10,21):
    print(number)
# 打印出每一个字母,每行一个字母
for letter in 'inty':
    print(letter)
# 每个字母前加上each letter is
for letter in 'inty':
    print("each letter is"+letter)